      GoogleFonts.getFont(poppins, fontSize: 16.0);
  static TextStyle get poppins24 =>
      GoogleFonts.getFont(poppins, fontSize: 24.0);

  static TextStyle get inter12 =>
      GoogleFonts.getFont(inter, fontSize: 12.0);
  static TextStyle get inter14 =>
      GoogleFonts.getFont(inter, fontSize: 14.0);
  static TextStyle get inter16 =>
      GoogleFonts.getFont(inter, fontSize: 16.0);
